username = "Abhiram"
console.log("Character at 1st position:", username.charAt(0));
console.log("Length of username:", username.length);
console.log("Trimmed username:", username.trim());
console.log("Username in uppercase:", username.toUpperCase());
console.log("Username in lowercase:", username.toLowerCase());
console.log("Username repeated 3 times:", username.repeat(3));
console.log("Does username start with 'b':", username.startsWith('b'));
console.log("Does username end with 'm':", username.endsWith('m'));
console.log("Is 'b' included in username:", username.includes('b'));
console.log("Replace all 'b' with '2' in username:", username.replaceAll('b', '2'));
console.log("Pad start of username with '33' until length is 20:", username.padStart(20,"33"));
console.log("Replace all 'b' with '@' in username:", username.replaceAll("b","@"));