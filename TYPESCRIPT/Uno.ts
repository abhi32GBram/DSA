// Define a new type 'person' with a single property 'name' of type string
type person = {
    name: string // The name property is expected to hold a string value
}

// Declare a constant variable 'person' of type 'person'
const person: person = {
    name: "abhiram" // Assign the string "abhiram" to the name property
}

// Log the value of the 'name' property of the 'person' object to the console
console.log(person.name) // Output: "abhiram"

